@Composable
fun DisplayImage(src: String, modifier: Modifier = Modifier) {
      AsyncImage(
          model = ImageRequest.Builder(LocalContext.current)
              .data(src)
              .crossfade(true)
              .build(),
          contentDescription = "",
          contentScale = ContentScale.Crop,
          modifier = Modifier.size(225.dp)
      )
}

